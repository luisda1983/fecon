app.controller('dtmnListCtrl', function($scope, $q, srv, comc, ctxl) {

	//*************************************************************************************************************//
	//*************************************************************************************************************//
	//****************************************                             ****************************************//
	//****************************************  FUNCIONES DE ARQUITECTURA  ****************************************//
	//****************************************                             ****************************************//
	//*************************************************************************************************************//
	//*************************************************************************************************************//

	//*************************************************************************************************************//
	// Carga de vista                                                                                              //
	//*************************************************************************************************************//
	function loadView() {
		
		var srv1 = comc.requestLiteList('BOOL'    , $scope.cntx);
		var srv2 = comc.request('dtmn/list', $scope.cntx);
		
		$q.all([srv.stResp(true, srv1, srv2)]).then(function(){
			$scope.cntx.conf.set('mode', 'L');
			view();
		});
	}

	//*************************************************************************************************************//
	// Carga de vista, en transición de retorno                                                                    //
	//*************************************************************************************************************//
	function loadViewBack() {
		var view = srv.getDestView();

		//Si venimos del formulario de categorías y tenemos una categoría en el área de intercambio, lo cargamos en la lista
		if (view === 'dtmn/form') {
			var dtmn = $scope.cntx.xchg.get('dtmn');
			if (dtmn !== 'undefined' && dtmn !== null) {
				var indx = $scope.cntx.xchg.get('indx')
				$scope.cntx.data.get('dtmnList')[indx] = dtmn;
			}
		}
	}
	
	//*************************************************************************************************************//
	// Carga de vista, en transición con datos                                                                     //
	//*************************************************************************************************************//
	function loadViewGo() {
		var view = srv.getOrigView();
		
		if (view === 'ctmn/list') {
			var ctmn = $scope.cntx.xchg.get('ctmn');
			if (ctmn !== 'undefined' && ctmn !== null) {
				$scope.cntx.form.get('ctmn').data = ctmn.iden;
				$scope.cntx.data.set('ctmn', ctmn);
			}
			$scope.cntx.data.set('ctmnList', $scope.cntx.xchg.get('ctmnList'));
		}
		loadView();
	}

	//*************************************************************************************************************//
	// Función encargada de manejar la vista y sus modos de presentación                                           //
	// - 'L': Lista de invitaciones.                                                                               //
	//*************************************************************************************************************//
	function view() {
		if ($scope.cntx.conf.get('mode') === 'L') {
			ctxl.formField($scope.cntx, 'ctmn', true, false);
			ctxl.formField($scope.cntx, 'iden', false, true);
		} else {
			ctxl.formField($scope.cntx, 'ctmn', false, true);
			ctxl.formField($scope.cntx, 'iden', false, true);
		}
	}

	//*************************************************************************************************************//
	// Función de inicialización del formulario de la vista                                                        //
	//*************************************************************************************************************//
	function inicForm() {
		$scope.cntx.form.get('ctmn').data = 0;
		$scope.cntx.form.get('iden').data = 0;
	}

	//*************************************************************************************************************//
	//*************************************************************************************************************//
	//***************************************                                **************************************//
	//***************************************  FUNCIONES DE MANEJO DE VISTA  **************************************//
	//***************************************                                **************************************//
	//*************************************************************************************************************//
	//*************************************************************************************************************//

	//*************************************************************************************************************//
	// Captura del evento de cambio en la categoría de menú.                                                       //
	//*************************************************************************************************************//
	$scope.fnCtmnChng = function() {
		var srv1 = comc.request('dtmn/list', $scope.cntx);
		srv.stResp(true, srv1);
	}

	//*************************************************************************************************************//
	// Captura del evento de modificación de detalle de menú.                                                      //
	//*************************************************************************************************************//
	$scope.fnDtmnModi = function(i) {
		var cntx = srv.getCntx('dtmn/form');
		var dtmn = $scope.cntx.data.get('dtmnList')[i];
		cntx.xchg.set('dtmn', dtmn);
		cntx.xchg.set('indx', i);
		cntx.xchg.set('ctmnList', $scope.cntx.data.get('ctmnList'));
		srv.go('dtmn/list', $scope.cntx, 'dtmn/form', cntx);
	}
	
	//*************************************************************************************************************//
	// Captura del evento de desactivación de detalle de menú.                                                     //
	//*************************************************************************************************************//
	$scope.fnDtmnDesa = function(i) {
		var dtmn = $scope.cntx.data.get('dtmnList')[i];
		$scope.cntx.form.get('iden').data = dtmn.iden;
		var srv1 = comc.request('dtmn/desa', $scope.cntx);
		
		$q.all([srv.stResp(true, srv1)]).then(function() {
			$scope.cntx.data.get('dtmnList')[i] = $scope.cntx.data.get('dtmn');
		});
	}
	
	//*************************************************************************************************************//
	// Captura del evento de activación de detalle de menú.                                                        //
	//*************************************************************************************************************//
	$scope.fnDtmnActi = function(i) {
		var dtmn = $scope.cntx.data.get('dtmnList')[i];
		$scope.cntx.form.get('iden').data = dtmn.iden;
		var srv1 = comc.request('dtmn/acti', $scope.cntx);
		
		$q.all([srv.stResp(true, srv1)]).then(function() {
			$scope.cntx.data.get('dtmnList')[i] = $scope.cntx.data.get('dtmn');
		});
	}

	//*************************************************************************************************************//
	// Captura del evento de cancelación.                                                                          //
	//*************************************************************************************************************//
	$scope.fnCanc = function(i) {
		srv.back(true, null);
	}

	//*************************************************************************************************************//
	//*************************************************************************************************************//
	//*********************************                                           *********************************//
	//*********************************  FUNCIONES AUXILIARES DE MANEJO DE VISTA  *********************************//
	//*********************************               (LISTAS)                    *********************************//
	//*********************************                                           *********************************//
	//*************************************************************************************************************//
	//*************************************************************************************************************//

	//*************************************************************************************************************//
	// Despliegue de menú de acciones.                                                                             //
	//*************************************************************************************************************//
	$scope.openMenu = function($mdOpenMenu, ev) {
		//FIXME: en versiones recientes de angular cambiar mdOpenMenu por mdMenu, y la apertura es mdMenu.open(ev)
		$mdOpenMenu(ev);
	};

	//*************************************************************************************************************//
	// Despliegue de registro de una lista.                                                                        //
	//*************************************************************************************************************//
	$scope.xpnd = function(i) {
		if ($scope.cntx.conf.get('idx1') === i) {
			$scope.cntx.conf.set('idx1', -1);
		} else {
			$scope.cntx.conf.set('idx1', i);
		}
	}

	//*************************************************************************************************************//
	//*************************************************************************************************************//
	//*********************************************                  **********************************************//
	//*********************************************  CARGA DE VISTA  **********************************************//
	//*********************************************                  **********************************************//
	//*************************************************************************************************************//
	//*************************************************************************************************************//
	
	//Generamos el contexto de la vista
	$scope.cntx = srv.getCntx('dtmn/list');

	if (srv.goTran()) {
		loadViewGo();
	} else if (srv.backTran()) {
		loadViewBack();
	} else {
		loadView();
	}

});