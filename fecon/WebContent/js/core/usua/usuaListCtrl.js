app.controller('usuaListCtrl', function($scope, $q, srv, comc, ctxl) {

	//*************************************************************************************************************//
	//*************************************************************************************************************//
	//****************************************                             ****************************************//
	//****************************************  FUNCIONES DE ARQUITECTURA  ****************************************//
	//****************************************                             ****************************************//
	//*************************************************************************************************************//
	//*************************************************************************************************************//

	//*************************************************************************************************************//
	// Carga de vista                                                                                              //
	//*************************************************************************************************************//
	function loadView() {
		
		var srv1 = comc.requestLiteList('BOOL', $scope.cntx);
		var srv2 = comc.request('usua/list', $scope.cntx);
		
		$q.all([srv.stResp(true, srv1, srv2)]).then(function(){
			$scope.cntx.conf.set('mode', 'L');
			view();
		});
	}

	//*************************************************************************************************************//
	// Carga de vista, en transición de retorno                                                                    //
	//*************************************************************************************************************//
	function loadViewBack() {
		var view = srv.getDestView();
		//Sin acciones adicionales
	}
	
	//*************************************************************************************************************//
	// Carga de vista, en transición con datos                                                                     //
	//*************************************************************************************************************//
	function loadViewGo() {
		var view = srv.getOrigView();
		if (view === 'inst/list') {
			var inst = $scope.cntx.xchg.get('inst');
			if (inst === null || inst === 'undefined') {
				srv.back(true, null);
			} else {
				$scope.cntx.form.get('inst').data = inst.iden;
				$scope.cntx.conf.set('back', true);
			}
		}
		loadView();
	}

	//*************************************************************************************************************//
	// Función encargada de manejar la vista y sus modos de presentación                                           //
	// - 'L': Lista de usuarios.                                                                               //
	//*************************************************************************************************************//
	function view() {
		if ($scope.cntx.conf.get('mode') === 'L') {
			ctxl.formField($scope.cntx, 'inst', false, true);
			ctxl.formField($scope.cntx, 'usua', false, true);
			ctxl.formField($scope.cntx, 'srch', true, false);
		} else {
			ctxl.formField($scope.cntx, 'inst', false, true);
			ctxl.formField($scope.cntx, 'usua', false, true);
			ctxl.formField($scope.cntx, 'srch', false, true);
		}
	}

	//*************************************************************************************************************//
	// Función de inicialización del formulario de la vista                                                        //
	//*************************************************************************************************************//
	function inicForm() {
		$scope.cntx.form.get('inst').data = 0;
		$scope.cntx.form.get('usua').data = '';
		$scope.cntx.form.get('srch').data = '';
	}

	//*************************************************************************************************************//
	//*************************************************************************************************************//
	//***************************************                                **************************************//
	//***************************************  FUNCIONES DE MANEJO DE VISTA  **************************************//
	//***************************************                                **************************************//
	//*************************************************************************************************************//
	//*************************************************************************************************************//

	//*************************************************************************************************************//
	// Captura del evento de cancelación.                                                                          //
	//*************************************************************************************************************//
	$scope.fnCanc = function(i) {
		srv.back(true, null);
	}

	//*************************************************************************************************************//
	// Captura del evento de activación de usuario.                                                                //
	//*************************************************************************************************************//
	$scope.fnUsuaActi = function(i) {
		var usua = $scope.cntx.data.get('usuaList')[i];
		$scope.cntx.form.get('usua').data = usua.iden;
		var srv1 = comc.request('usua/acti', $scope.cntx);
		
		$q.all([srv.stResp(true, srv1)]).then(function() {
			$scope.cntx.data.get('usuaList')[i] = $scope.cntx.data.get('usua');
		});
		
	}

	//*************************************************************************************************************//
	// Captura del evento de desactivación de usuario.                                                             //
	//*************************************************************************************************************//
	$scope.fnUsuaInac = function(i) {
		var usua = $scope.cntx.data.get('usuaList')[i];
		$scope.cntx.form.get('usua').data = usua.iden;
		var srv1 = comc.request('usua/inac', $scope.cntx);
		
		$q.all([srv.stResp(true, srv1)]).then(function() {
			$scope.cntx.data.get('usuaList')[i] = $scope.cntx.data.get('usua');
		});
		
	}
	
	//*************************************************************************************************************//
	//*************************************************************************************************************//
	//*********************************                                           *********************************//
	//*********************************  FUNCIONES AUXILIARES DE MANEJO DE VISTA  *********************************//
	//*********************************               (LISTAS)                    *********************************//
	//*********************************                                           *********************************//
	//*************************************************************************************************************//
	//*************************************************************************************************************//

	//*************************************************************************************************************//
	// Despliegue de menú de acciones.                                                                             //
	//*************************************************************************************************************//
	$scope.openMenu = function($mdOpenMenu, ev) {
		//FIXME: en versiones recientes de angular cambiar mdOpenMenu por mdMenu, y la apertura es mdMenu.open(ev)
		$mdOpenMenu(ev);
	};

	//*************************************************************************************************************//
	// Despliegue de registro de una lista.                                                                        //
	//*************************************************************************************************************//
	$scope.xpnd = function(i) {
		if ($scope.cntx.conf.get('idx1') === i) {
			$scope.cntx.conf.set('idx1', -1);
		} else {
			$scope.cntx.conf.set('idx1', i);
		}
	}

	//*************************************************************************************************************//
	//*************************************************************************************************************//
	//*********************************************                  **********************************************//
	//*********************************************  CARGA DE VISTA  **********************************************//
	//*********************************************                  **********************************************//
	//*************************************************************************************************************//
	//*************************************************************************************************************//
	
	//Generamos el contexto de la vista
	$scope.cntx = srv.getCntx('usua/list');

	if (srv.goTran()) {
		loadViewGo();
	} else if (srv.backTran()) {
		loadViewBack();
	} else {
		loadView();
	}

});