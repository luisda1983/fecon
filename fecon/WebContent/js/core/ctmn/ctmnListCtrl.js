app.controller('ctmnListCtrl', function($scope, $q, srv, comc, ctxl) {

	//*************************************************************************************************************//
	//*************************************************************************************************************//
	//****************************************                             ****************************************//
	//****************************************  FUNCIONES DE ARQUITECTURA  ****************************************//
	//****************************************                             ****************************************//
	//*************************************************************************************************************//
	//*************************************************************************************************************//

	//*************************************************************************************************************//
	// Carga de vista                                                                                              //
	//*************************************************************************************************************//
	function loadView() {		
		var srv1 = comc.requestLiteList('MENUPERF', $scope.cntx);
		var srv2 = comc.requestLiteList('BOOL'    , $scope.cntx);
		
		$q.all([srv.stResp(false, srv1)]).then(function(){
			$scope.cntx.form.get('perf').data = $scope.cntx.data.get('ltLMenuperf')[0].clav;
			var srv3 = comc.request('ctmn/list', $scope.cntx);
			
			$q.all([srv.stResp(true, srv3)]).then(function(){
				view();
			});
		});
	}

	//*************************************************************************************************************//
	// Carga de vista, en transición de retorno                                                                    //
	//*************************************************************************************************************//
	function loadViewBack() {
		var view = srv.getDestView();

		//Si venimos del formulario de categorías y tenemos una categoría en el área de intercambio, lo cargamos en la lista
		if (view === 'ctmn/form') {
			var ctmn = $scope.cntx.xchg.get('ctmn');
			if (ctmn !== 'undefined' && ctmn !== null) {
				var indx = $scope.cntx.xchg.get('indx')
				$scope.cntx.data.get('ctmnList')[indx] = ctmn;
			}
		}
		
		ctxl.clearXchg($scope.cntx);
	}
	
	//*************************************************************************************************************//
	// Carga de vista, en transición con datos                                                                     //
	//*************************************************************************************************************//
	function loadViewGo() {
		var view = srv.getOrigView();
		
		$scope.cntx.conf.set('mode', 'L');
		
		loadView();
	}

	//*************************************************************************************************************//
	// Función encargada de manejar la vista y sus modos de presentación                                           //
	// - 'L': Lista de invitaciones.                                                                               //
	//*************************************************************************************************************//
	function view() {
		if ($scope.cntx.conf.get('mode') === 'L') {
			ctxl.formField($scope.cntx, 'perf', true, false);
		} else {
			ctxl.formField($scope.cntx, 'perf', false, true);
		}
	}

	//*************************************************************************************************************//
	// Función de inicialización del formulario de la vista                                                        //
	//*************************************************************************************************************//
	function inicForm() {
		$scope.cntx.form.get('perf').data = '';
	}

	//*************************************************************************************************************//
	//*************************************************************************************************************//
	//***************************************                                **************************************//
	//***************************************  FUNCIONES DE MANEJO DE VISTA  **************************************//
	//***************************************                                **************************************//
	//*************************************************************************************************************//
	//*************************************************************************************************************//

	//*************************************************************************************************************//
	// Captura del evento de cambio en el desplegable perfil.                                                      //
	//*************************************************************************************************************//
	$scope.fnPerfChng = function() {
		var srv1 = comc.request('ctmn/list', $scope.cntx);
		
		srv.stResp(true, srv1);
	}

	//*************************************************************************************************************//
	// Captura del evento de consultar detalle de menú.                                                            //
	//*************************************************************************************************************//
	$scope.fnCtmnDtmn = function(i) {
		var cntx = srv.getCntx('dtmn/list');
		
		cntx.xchg.set('ctmn', $scope.cntx.data.get('ctmnList')[i]);
		cntx.xchg.set('ctmnList', $scope.cntx.data.get('ctmnList'));
		
		srv.go('ctmn/list', $scope.cntx, 'dtmn/list', cntx);
	}
	
	//*************************************************************************************************************//
	// Captura del evento de modificación de categoría.                                                            //
	//*************************************************************************************************************//
	$scope.fnCtmnModi = function(i) {
		var cntx = srv.getCntx('ctmn/form');
		
		var ctmn = $scope.cntx.data.get('ctmnList')[i];
		cntx.xchg.set('ctmn', ctmn);
		cntx.xchg.set('indx', i);
		
		srv.go('ctmn/list', $scope.cntx, 'ctmn/form', cntx);
	}
	
	//*************************************************************************************************************//
	// Captura del evento de desactivación de categoría de menú.                                                   //
	//*************************************************************************************************************//
	$scope.fnCtmnDesa = function(i) {
		var ctmn = $scope.cntx.data.get('ctmnList')[i];
		$scope.cntx.form.get('iden').data = ctmn.iden;
		
		var srv1 = comc.request('ctmn/desa', $scope.cntx);
		
		$q.all([srv.stResp(true, srv1)]).then(function() {
			$scope.cntx.data.get('ctmnList')[i] = $scope.cntx.data.get('ctmn');
		});
	}
	
	//*************************************************************************************************************//
	// Captura del evento de activación de categoría de menú.                                                      //
	//*************************************************************************************************************//
	$scope.fnCtmnActi = function(i) {
		var ctmn = $scope.cntx.data.get('ctmnList')[i];
		$scope.cntx.form.get('iden').data = ctmn.iden;
		
		var srv1 = comc.request('ctmn/acti', $scope.cntx);
		
		$q.all([srv.stResp(true, srv1)]).then(function() {
			$scope.cntx.data.get('ctmnList')[i] = $scope.cntx.data.get('ctmn');
		});
	}

	//*************************************************************************************************************//
	//*************************************************************************************************************//
	//*********************************                                           *********************************//
	//*********************************  FUNCIONES AUXILIARES DE MANEJO DE VISTA  *********************************//
	//*********************************               (LISTAS)                    *********************************//
	//*********************************                                           *********************************//
	//*************************************************************************************************************//
	//*************************************************************************************************************//

	//*************************************************************************************************************//
	// Despliegue de menú de acciones.                                                                             //
	//*************************************************************************************************************//
	$scope.openMenu = function($mdOpenMenu, ev) {
		//FIXME: en versiones recientes de angular cambiar mdOpenMenu por mdMenu, y la apertura es mdMenu.open(ev)
		$mdOpenMenu(ev);
	};

	//*************************************************************************************************************//
	// Despliegue de registro de una lista.                                                                        //
	//*************************************************************************************************************//
	$scope.xpnd = function(i) {
		if ($scope.cntx.conf.get('idx1') === i) {
			$scope.cntx.conf.set('idx1', -1);
		} else {
			$scope.cntx.conf.set('idx1', i);
		}
	}

	//*************************************************************************************************************//
	//*************************************************************************************************************//
	//*********************************************                  **********************************************//
	//*********************************************  CARGA DE VISTA  **********************************************//
	//*********************************************                  **********************************************//
	//*************************************************************************************************************//
	//*************************************************************************************************************//
	
	//Generamos el contexto de la vista
	$scope.cntx = srv.getCntx('ctmn/list');

	if (srv.goTran()) {
		loadViewGo();
	} else if (srv.backTran()) {
		loadViewBack();
	} else {
		loadView();
	}
});