app.controller('deleFormCtrl', function($scope, $q, srv, comc, ctxl) {

	//*************************************************************************************************************//
	//*************************************************************************************************************//
	//****************************************                             ****************************************//
	//****************************************  FUNCIONES DE ARQUITECTURA  ****************************************//
	//****************************************                             ****************************************//
	//*************************************************************************************************************//
	//*************************************************************************************************************//

	//*************************************************************************************************************//
	// Carga de vista                                                                                              //
	//*************************************************************************************************************//
	function loadView() {
		view();
	}

	//*************************************************************************************************************//
	// Carga de vista, en transición de retorno                                                                    //
	//*************************************************************************************************************//
	function loadViewBack() {
		var view = srv.getDestView();
		
		ctxl.clearXchg($scope.cntx);
	}

	//*************************************************************************************************************//
	// Carga de vista, en transición con datos                                                                     //
	//*************************************************************************************************************//
	function loadViewGo() {
		var view = srv.getOrigView();
		
		$scope.cntx.conf.set('mode', 'N');
		
		if (view === 'dele/list') {
			var dele = $scope.cntx.xchg.get('dele');

			if (dele === undefined) {
				$scope.cntx.conf.set('mode', 'N');
				
				var domi = $scope.cntx.xchg.get('domi');
				$scope.cntx.data.set('domi', domi);
				
				$scope.cntx.form.get('domi').data = domi.nomb;
			} else {
				$scope.cntx.conf.set('mode', 'E');
				
				var domi = $scope.cntx.xchg.get('domi');
				$scope.cntx.data.set('domi', domi);
				
				$scope.cntx.form.get('iden').data = dele.iden;
				$scope.cntx.form.get('domi').data = dele.domi;
				$scope.cntx.form.get('valo').data = dele.valo;
			}
		}
		
		loadView();
	}

	//*************************************************************************************************************//
	// Función encargada de manejar la vista y sus modos de presentación                                           //
	// - 'N': Nuevo elemento de dominio.                                                                           //
	// - 'E': Edición de elemento de dominio.                                                                      //
	//*************************************************************************************************************//
	function view() {
		//Nuevo elemento de dominio
		if ($scope.cntx.conf.get('mode') === 'N') {
			ctxl.formField($scope.cntx, 'iden', false, true);
			ctxl.formField($scope.cntx, 'domi', false, true);
			ctxl.formField($scope.cntx, 'valo', true, false);
		//Edición de elemento de dominio
		} else if ($scope.cntx.conf.get('mode') === 'E') {
			ctxl.formField($scope.cntx, 'iden', true, true);
			ctxl.formField($scope.cntx, 'domi', false, true);
			ctxl.formField($scope.cntx, 'valo', true, false);
		}
	}

	//*************************************************************************************************************//
	// Función de inicialización del formulario de la vista                                                        //
	//*************************************************************************************************************//
	function inicForm() {
		$scope.cntx.form.get('iden').data = 0;
		//$scope.cntx.form.get('valo').data = ''; -- No se inicializa la categoría pues viene impuesta por flujo
		$scope.cntx.form.get('valo').data = '';
	}

	//*************************************************************************************************************//
	//*************************************************************************************************************//
	//***************************************                                **************************************//
	//***************************************  FUNCIONES DE MANEJO DE VISTA  **************************************//
	//***************************************                                **************************************//
	//*************************************************************************************************************//
	//*************************************************************************************************************//

	//*************************************************************************************************************//
	// Captura del evento de cancelación.                                                                          //
	//*************************************************************************************************************//
	$scope.fnCanc = function() {
		srv.back(true, null);
	}

	//*************************************************************************************************************//
	// Captura del evento de envío de formulario de elemento de dominio.                                           //
	//*************************************************************************************************************//
	$scope.fnForm = function() {
		var srv1 = comc.request('dele/form', $scope.cntx);
		
		$q.all([srv.stResp(true, srv1)]).then(function(){
			$scope.cntx.xchg.set('dele', $scope.cntx.data.get('dele'));
			srv.back(true, $scope.cntx.xchg);
		});
	};

	//*************************************************************************************************************//
	//*************************************************************************************************************//
	//*********************************************                  **********************************************//
	//*********************************************  CARGA DE VISTA  **********************************************//
	//*********************************************                  **********************************************//
	//*************************************************************************************************************//
	//*************************************************************************************************************//
	
	$scope.cntx = srv.getCntx('dele/form');

	if (srv.goTran()) {
		loadViewGo();
	} else if (srv.backTran()) {
		loadViewBack();
	} else {
		loadView();
	}	
});