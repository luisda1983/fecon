app.controller('deleListCtrl', function($scope, $q, srv, comc, ctxl) {

	//*************************************************************************************************************//
	//*************************************************************************************************************//
	//****************************************                             ****************************************//
	//****************************************  FUNCIONES DE ARQUITECTURA  ****************************************//
	//****************************************                             ****************************************//
	//*************************************************************************************************************//
	//*************************************************************************************************************//

	//*************************************************************************************************************//
	// Carga de vista                                                                                              //
	//*************************************************************************************************************//
	function loadView() {
		var srv1 = comc.request('dele/list', $scope.cntx);

		$q.all([srv.stResp(true, srv1)]).then(function() {
			view();
		});	
	}
	
	//*************************************************************************************************************//
	// Carga de vista, en transición de retorno                                                                    //
	//*************************************************************************************************************//
	function loadViewBack() {
		var view = srv.getDestView();
		
		//Si venimos del formulario de detalle de dominio y tenemos uno en el área de intercambio, lo cargamos en la lista
		if (view === 'dele/form') {
			var dele = $scope.cntx.xchg.get('dele');

			if (dele !== undefined) {
				var indx = $scope.cntx.xchg.get('indx');
				if (indx >= 0) {
					$scope.cntx.data.get('deleList')[indx] = dele;
				} else {
					$scope.cntx.data.get('deleList').push(dele);
				}
			}
		}
		
		ctxl.clearXchg($scope.cntx);
	}
	
	//*************************************************************************************************************//
	// Carga de vista, en transición con datos                                                                     //
	//*************************************************************************************************************//
	function loadViewGo() {
		var view = srv.getOrigView();
		
		$scope.cntx.conf.set('mode', 'L');
		
		if (view === 'domi/list') {
			var domi = $scope.cntx.xchg.get('domi');
			$scope.cntx.data.set('domi', domi);
			$scope.cntx.form.get('domi').data = domi.nomb;
		}
		
		loadView();
	}

	//*************************************************************************************************************//
	// Función encargada de manejar la vista y sus modos de presentación                                           //
	// - 'L': Lista de detalle de dominio.                                                                         //
	//*************************************************************************************************************//
	function view() {
		if ($scope.cntx.conf.get('mode') === 'L') {
			ctxl.formField($scope.cntx, 'domi', false, true);
		} else {
			ctxl.formField($scope.cntx, 'domi', false, true);
		}
	}

	//*************************************************************************************************************//
	// Función de inicialización del formulario de la vista                                                        //
	//*************************************************************************************************************//
	function inicForm() {
		$scope.cntx.form.get('domi').data = 0;
	}

	//*************************************************************************************************************//
	//*************************************************************************************************************//
	//*********************************                                           *********************************//
	//*********************************  FUNCIONES AUXILIARES DE MANEJO DE VISTA  *********************************//
	//*********************************               (LISTAS)                    *********************************//
	//*********************************                                           *********************************//
	//*************************************************************************************************************//
	//*************************************************************************************************************//

	//*************************************************************************************************************//
	// Captura del evento de crear nuevo detalle de dominio.                                                       //
	//*************************************************************************************************************//
	$scope.fnNuev = function() {
		var cntx = srv.getCntx('dele/form');
		
		cntx.xchg.set('domi', $scope.cntx.data.get('domi'));
		cntx.xchg.set('indx', -1);
		
		srv.go('dele/list', $scope.cntx, 'dele/form', cntx);
	}

	//*************************************************************************************************************//
	// Captura del evento de edición de detalle de dominio.                                                        //
	//*************************************************************************************************************//
	$scope.fnEdit = function(i) {
		var cntx = srv.getCntx('dele/form');
		
		var dele = $scope.cntx.data.get('deleList')[i];
		cntx.xchg.set('domi', $scope.cntx.data.get('domi'));
		cntx.xchg.set('dele', dele);
		cntx.xchg.set('indx', i);
		
		srv.go('dele/list', $scope.cntx, 'dele/form', cntx);
	}

	//*************************************************************************************************************//
	// Captura del evento de cancelación.                                                                          //
	//*************************************************************************************************************//
	$scope.fnCanc = function() {
		srv.back(true, null);
	}

	//*************************************************************************************************************//
	// Despliegue de menú de acciones.                                                                             //
	//*************************************************************************************************************//
	$scope.openMenu = function($mdOpenMenu, ev) {
		//FIXME: en versiones recientes de angular cambiar mdOpenMenu por mdMenu, y la apertura es mdMenu.open(ev)
		$mdOpenMenu(ev);
	};

	//*************************************************************************************************************//
	// Despliegue de registro de una lista.                                                                        //
	//*************************************************************************************************************//
	$scope.xpnd = function(i) {
		if ($scope.cntx.conf.get('idx1') === i) {
			$scope.cntx.conf.set('idx1', -1);
		} else {
			$scope.cntx.conf.set('idx1', i);
		}
	}

	//*************************************************************************************************************//
	//*************************************************************************************************************//
	//*********************************************                  **********************************************//
	//*********************************************  CARGA DE VISTA  **********************************************//
	//*********************************************                  **********************************************//
	//*************************************************************************************************************//
	//*************************************************************************************************************//
	
	//Generamos el contexto de la vista
	$scope.cntx = srv.getCntx('dele/list');

	if (srv.goTran()) {
		loadViewGo();
	} else if (srv.backTran()) {
		loadViewBack();
	} else {
		loadView();
	}
});